class Solution:

    def isValidSudoku(self, board):

        result = []
        for i in range(9):
            for j in range(9):

                element = board[i][j]

                if element != ".":
                    # extend
                    result += [(i, element), (element, j), (i, j, element)]

        return len(result) == len(set(result))

